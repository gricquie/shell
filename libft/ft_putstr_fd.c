#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int i;

	i = 0;
	if (fd > 0)
	{
		if (!s)
			write(fd, &s[i++], 1);
		else
			write(1, s, ft_strlen(s));
	}
}
